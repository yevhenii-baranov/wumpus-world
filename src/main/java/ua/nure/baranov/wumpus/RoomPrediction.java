package ua.nure.baranov.wumpus;

class RoomPrediction {
    private Boolean exist;
    private Boolean stench;
    private Boolean breeze;
    private Boolean pit;
    private Boolean wampus;
    private Boolean ok;
    private Boolean gold;
    private Boolean noWay;

    public RoomPrediction() { }

    public Boolean getExist() {
        return exist;
    }

    public void setExist(Boolean exist) {
        this.exist = exist;
    }

    public Boolean getStench() {
        return stench;
    }

    public void setStench(Boolean stench) {
        this.stench = stench;
    }

    public Boolean getBreeze() {
        return breeze;
    }

    public void setBreeze(Boolean breeze) {
        this.breeze = breeze;
    }

    public Boolean getPit() {
        return pit;
    }

    public void setPit(Boolean pit) {
        this.pit = pit;
    }

    public Boolean getWampus() {
        return wampus;
    }

    public void setWampus(Boolean wampus) {
        this.wampus = wampus;
    }

    public Boolean getOk() {
        return ok;
    }

    public void setOk(Boolean ok) {
        this.ok = ok;
    }

    public Boolean getGold() {
        return gold;
    }

    public void setGold(Boolean gold) {
        this.gold = gold;
    }

    public Boolean getNoWay() {
        return noWay;
    }

    public void setNoWay(Boolean noWay) {
        this.noWay = noWay;
    }
}
